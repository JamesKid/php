<?php
/* 这不是真正的函数，是语言结构，list()用一步操作给一组变量进行
 *
 */
list($a,$b)=array(10,20);
echo $a,'~',$b,'<br />';
//返回10~20

list($a,$b,,$c)=array(2=>10,3=>20,4=>30,1=>40);
echo $a,'~',$b,'~',$c,'<br />';
//返回notice~40~20
//执行到$a的时候返回给我一个notice：说数组没有0键

/* each 和　list 实现数组的遍历 */

$arr = array('你','若','安','好','便','是','晴','天');
for(;list($k,$v)=each($arr);){
    echo $k,'~',$v,'<br />';
}
/*
return:
0~你
1~若
2~安
3~好
4~便
5~是
6~晴
7~天
*/
?>
